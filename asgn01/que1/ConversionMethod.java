
public class ConversionMethod {

	public String toBinaryString(int b) {
		String bin = Integer.toBinaryString(b);

		return bin;
	}

	String toOctalString(int o) {
		String octa = Integer.toOctalString(o);

		return octa;
	}

	String toHexString(int h) {
		String hex = Integer.toHexString(h);

		return hex;
	}

}
